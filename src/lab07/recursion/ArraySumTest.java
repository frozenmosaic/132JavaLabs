package lab07.recursion;

import static org.junit.Assert.*;

import java.util.Random;

import org.junit.Test;

public class ArraySumTest {
	
	private ArraySum arrSum = new ArraySum();
	
	@Test
	public void testArraySum1() {
		int[] arr1 = new int[4];
		arr1[0] = 2; 
		arr1[1] = 7;
		arr1[2] = 19;
		arr1[3] = 20;
		assertEquals(48, arrSum.arraySum(arr1));	
	}
	
	@Test
	public void testArraySum2() {
		int[] arr1 = new int[5];
		arr1[0] = 2; 
		arr1[1] = 7;
		arr1[2] = 19;
		arr1[3] = 20;
		arr1[4] = -5;
		assertEquals(43, arrSum.arraySum(arr1));		
	}
	
	

	
}
